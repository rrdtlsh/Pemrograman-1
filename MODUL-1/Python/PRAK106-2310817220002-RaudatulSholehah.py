a = int(input("Variabel a bernilai :"))
b = int(input("Variabel b bernilai :"))
c = int(input("Variabel c bernilai :"))

a_sama_dengan_b = a == b
b_lebihbesardari_c = b > c
a_tidaksamadengan_c = a != c

print(f"Apakah a sama dengan b ? jawabannya adalah {a_sama_dengan_b}")
print(f"Apakah b lebih besar dari c ? jawabannya adalah {b_lebihbesardari_c}")
print(f"Apakah a tidak sama dengan c ? jawabannya adalah {a_tidaksamadengan_c}")